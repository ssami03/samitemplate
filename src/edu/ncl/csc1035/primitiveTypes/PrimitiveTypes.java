package edu.ncl.csc1035.primitiveTypes;



public class PrimitiveTypes {
    public static void main(String[] args) {
        int i = 26;
        double d = 3.14;
        char c = 'a';
        boolean b = true;
        System.out.println(i);
        System.out.println(d);
        System.out.println(c);
        System.out.println(b);
    }
}
