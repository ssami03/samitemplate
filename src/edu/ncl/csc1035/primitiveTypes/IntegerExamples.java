package edu.ncl.csc1035.primitiveTypes;

public class IntegerExamples {
    public static void main(String[] args){

        int dec = 10;       // decimal value
        int bin = 0b1010;   // binary value - prepend 0b
        int oct = 012;      // octal value - prepend 0
        int hex = 0xA;      // hexadecimal value - prepend 0x

        // print values
        System.out.println(dec);
        System.out.println(bin);
        System.out.println(oct);
        System.out.println(hex);
    }
}
